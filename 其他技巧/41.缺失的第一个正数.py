class Solution:
    def firstMissingPositive(self, nums: List[int]) -> int:
        n = len(nums)

        # 原地哈希：将数值 k 放到索引 k-1 的位置
        # 即 nums[i] = k 时，应该放到 nums[k-1]
        for i in range(n):
            # 不断交换，直到当前位置的值不在 [1, n] 范围内，或者已经在正确位置
            while 1 <= nums[i] <= n and nums[nums[i] - 1] != nums[i]:
                j = nums[i] - 1
                nums[i], nums[j] = nums[j], nums[i]

        # 找第一个不在正确位置的索引
        for i in range(n):
            if nums[i] != i + 1:
                return i + 1

        # 所有位置都正确，缺失的是 n+1
        return n + 1


# ============================================================
# 核心思路：原地哈希
# ============================================================
#
# 问题：找最小的缺失正整数
# 关键洞察：如果数组长度为 n，缺失的正整数一定在 [1, n+1] 范围内
# - 如果 [1, n] 都出现了，缺失的是 n+1
# - 否则缺失的是 [1, n] 中没出现的最小值
#
# 思路：原地哈希（利用数组本身作为哈希表）
# - 将数值 k 放到索引 k-1 的位置（nums[k-1] = k）
# - 交换直到当前位置的值不在 [1, n] 范围内，或已经在正确位置
# - 最后遍历，找第一个 nums[i] != i+1 的位置
#
# 易错点：
# - 必须用 while 循环，不是 if（交换后当前位置可能又有新的值需要处理）
# - 交换前先保存 j = nums[i] - 1，避免交换后 nums[i] 变了
# - 条件 nums[nums[i]-1] != nums[i]：目标位置还不是正确的值
#
# 时间复杂度：O(n)，每个元素最多被交换一次到正确位置
# 空间复杂度：O(1)，原地修改


# 验证：nums = [3, 4, -1, 1]
# - i=0: nums[0]=3, j=2, 交换 nums[0] 和 nums[2] -> [-1, 4, 3, 1]
#   nums[0]=-1 不在 [1,4]，停止
# - i=1: nums[1]=4, j=3, 交换 nums[1] 和 nums[3] -> [-1, 1, 3, 4]
#   nums[1]=1, j=0, 交换 nums[1] 和 nums[0] -> [1, -1, 3, 4]
#   nums[1]=-1 不在 [1,4]，停止
# - i=2: nums[2]=3, j=2, nums[2] 已在正确位置（nums[2]=3），停止
# - i=3: nums[3]=4, j=3, nums[3] 已在正确位置，停止
# 结果：[1, -1, 3, 4]
# 遍历：nums[1] != 2，返回 2