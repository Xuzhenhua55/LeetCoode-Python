"""
470. 用 Rand7() 实现 Rand10()
中等

给定方法 rand7 可生成 [1,7] 范围内的均匀随机整数，试写一个方法 rand10 生成 [1,10] 范围内的均匀随机整数。
你只能调用 rand7() 且不能调用其他方法。请不要使用系统的 Math.random() 方法。

每个测试用例将有一个内部参数 n，即你实现的函数 rand10() 在测试时将被调用的次数。请注意，这不是传递给 rand10() 的参数。

【核心思路：拒绝采样（Rejection Sampling）】
遇到这类 randX() 生成 randY() 的问题，都可以套用这个通用公式：
(randX() - 1) * X + randX() 可以等概率生成 [1, X * X] 范围内的随机数。然后再配合拒绝采样截取需要的范围即可。

【常见疑问解答】
1. 为什么不直接取 1-10 之间呢？
直接取 1-10 在数学上是完全正确的，能保证等概率。
但是效率太低。如果只接受 1-10，意味着要拒绝掉 11-49 这 39 个数字，接受率只有 10/49 ≈ 20.4%。
而取 1-40，只拒绝 41-49 这 9 个数字，接受率大幅提升到了 40/49 ≈ 81.6%。
因为 40 是 10 的整数倍，1-40 中的每个数字映射到 1-10 后，每个数字都有刚好 4 个数字对应它，保证了等概率。

2. 为什么是 (idx - 1) % 10 + 1，而不是直接 idx % 10？
我们需要生成的范围是 [1, 10]，而 idx % 10 生成的范围是 [0, 9]。
如果直接用 idx % 10，当 idx = 10 或 20 时，结果是 0，这不在我们需要的 [1, 10] 范围内。
(idx - 1) % 10 + 1 是一次平移操作：
- 先减 1：把 [1, 40] 平移到 [0, 39]
- 再取模：对 [0, 39] 取模 10，结果均匀落在 [0, 9]
- 最后加 1：把 [0, 9] 平移回 [1, 10]
"""

# The rand7() API is already defined for you.
# def rand7():
# @return a random integer in the range 1 to 7

class Solution:
    def rand10(self):
        """
        :rtype: int
        """
        while True:
            row = rand7()
            col = rand7()
            # 构造 1 到 49 的均匀随机数
            idx = (row - 1) * 7 + col
            
            # 如果在 1 到 40 之间，则映射到 1 到 10 并返回
            if idx <= 40:
                return (idx - 1) % 10 + 1
            
            # 如果是 41 到 49，则拒绝，进入下一次 while 循环重新生成

    # 进阶优化版（减少 rand7 的调用次数）
    def rand10_optimized(self):
        while True:
            row = rand7()
            col = rand7()
            idx = (row - 1) * 7 + col
            if idx <= 40:
                return (idx - 1) % 10 + 1
            
            # idx 此时在 41 到 49 之间
            row = idx - 40 # 得到 1 到 9 的随机数
            col = rand7()
            # 构造 1 到 63 的均匀随机数
            idx = (row - 1) * 7 + col
            if idx <= 60:
                return (idx - 1) % 10 + 1
            
            # idx 此时在 61 到 63 之间
            row = idx - 60 # 得到 1 到 3 的随机数
            col = rand7()
            # 构造 1 到 21 的均匀随机数
            idx = (row - 1) * 7 + col
            if idx <= 20:
                return (idx - 1) % 10 + 1
            
            # 如果 idx 是 21，只能重新开始循环了
